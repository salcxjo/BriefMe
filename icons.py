# ── Map OWM icon codes to bitmaps ─────────────────────────────────────────────
OWM_ICON_MAP = {
    "01d": SUN,
    "01n": SUN,
    "02d": CLOUD,
    "02n": CLOUD,
    "03d": CLOUD,
    "03n": CLOUD,
    "04d": CLOUD,
    "04n": CLOUD,
    "09d": RAIN,
    "09n": RAIN,
    "10d": RAIN,
    "10n": RAIN,
    "11d": THUNDER,
    "11n": THUNDER,
    "13d": SNOW,
    "13n": SNOW,
    "50d": MIST,
    "50n": MIST,
}

def get_icon_fb(icon_code: str):
    """Return a FrameBuffer for the given OWM icon code (or CLOUD as default)."""
    bitmap = OWM_ICON_MAP.get(icon_code, CLOUD)
    return framebuf.FrameBuffer(bitmap, 16, 16, framebuf.MONO_HLSB)
